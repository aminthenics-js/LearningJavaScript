    const month = "September";

    switch (month) {
        case "Jan":
            console.log("January");
            break;
        case "Feb":
            console.log("February");
            break;

        case "Mar":
            console.log("March");
            break;

        case "Apr":
            console.log("April");
            break;

        case "May":
            console.log("May");
            break;

        case "June":
            console.log("June");
            break;

        case "July":
            console.log("July");
            break;   

        default:
            console.log("Default case match");
            break;
    }